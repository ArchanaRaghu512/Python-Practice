class calculator:

    def addition(x,y):
        added = x + y
        print(added)

    def subtraction(x,y):
        sub = x - y
        print(sub)

    def multiplication(x,y):
        product = x * y
        print(product)

    def division(x,y):
        div = x / y
        print(div)

calculator.addition(2,3)
calculator.subtraction(5,1)
calculator.multiplication(3,10)
calculator.division(34,3)

